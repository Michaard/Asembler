$ NOMOD51
$ INCLUDE (8051_mgraniczka.MCU)

_ST_POINTER EQU 07Fh
CSDS16 EQU 0FF30h

ORG 0000h
_RESET:
	LJMP _INIT

ORG 0100h
_INIT:
	MOV SP,#_ST_POINTER
	;LCALL _LED_ON
	MOV DPL,#00h
	MOV DPH,#02h
	PUSH DPL
	PUSH DPH
	RET
_LOOP:
	NOP
	LJMP _LOOP
ORG 0200h
_LED_ON:
	CLR 097h
	LJMP _LOOP
	;RET
END